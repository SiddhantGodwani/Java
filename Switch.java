//Switch statements
public class Switch
    {
        public static void main(String args[])
        {
            int n = 7;
            switch(n){
                case 1,7:
                    System.out.println("M");
                    break;
                case 2:
                    System.out.println("T");
                    break;
                case 3:
                    System.out.println("F");
                    break;
                default:
                    System.out.print("Wrong input");
            };
        }
    }